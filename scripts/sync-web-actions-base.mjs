#!/usr/bin/env node
/**
 * Reads the first OpenWhisk package key under `application.runtimeManifest.packages`
 * in app.config.yaml and:
 * - Writes `web-src/src/llmWebActionsBase.generated.js` so the SPA can POST to
 *   `/api/v1/web/<package>/…` without hardcoding the package in brand.json.
 * - Writes `web-src/src/llmSpaBasename.generated.js` with `/<package>` for namespaced CDN paths, or
 *   **`""`** when **`LLM_STATIC_WEB_AT_ROOT=1`** at sync time (single SPA at static origin `/`).
 * - Sets Parcel `targets.default.publicUrl` in `web-src/package.json` to `/<package>/`
 *   (or `/` when no package, or always `/` when **`LLM_STATIC_WEB_AT_ROOT=1`**) so asset URLs match
 *   the Router basename / MCP deep links.
 * - When namespaced (package key and **not** `LLM_STATIC_WEB_AT_ROOT`), sets
 *   **`targets.default.distDir`** to `./dist/<package>` for a **standalone** `parcel build` in `web-src`.
 *   **`aio app build`** uses `@adobe/aio-lib-web`, which overrides Parcel `distDir` to **`web.distProd`** (flat);
 *   **`app.config.yaml`** **`post-app-build`** then moves that output under **`web-prod/<package>/`** and
 *   rewrites **`index.html`** if aio emitted root **`/web-src.*`** URLs (the **`webassets`** target ignores **`default.publicUrl`** unless mirrored under **`targets.webassets.publicUrl`** — sync sets both).
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const yamlPath = path.join(root, "app.config.yaml");
const outFile = path.join(root, "web-src", "src", "llmWebActionsBase.generated.js");
const basenameFile = path.join(root, "web-src", "src", "llmSpaBasename.generated.js");
const webPackageJson = path.join(root, "web-src", "package.json");

/**
 * @param {string} text
 * @returns {string} first package id or ""
 */
function extractFirstRuntimePackageKey(text) {
  const lines = text.split(/\r?\n/);
  let packagesIndent = -1;
  let seenPackages = false;

  for (const line of lines) {
    const m = /^(\s*)packages:\s*(#.*)?$/.exec(line);
    if (m) {
      seenPackages = true;
      packagesIndent = m[1].length;
      continue;
    }
    if (!seenPackages) {
      continue;
    }
    if (!line.trim() || /^\s*#/.test(line)) {
      continue;
    }
    const keyM = /^(\s*)([a-zA-Z0-9_.-]+):\s*(#.*)?$/.exec(line);
    if (!keyM) {
      continue;
    }
    const indent = keyM[1].length;
    if (indent <= packagesIndent) {
      break;
    }
    return keyM[2];
  }
  return "";
}

let pkg = "";
try {
  const text = fs.readFileSync(yamlPath, "utf8");
  pkg = extractFirstRuntimePackageKey(text);
} catch (e) {
  console.warn("[sync-web-actions-base] skipped:", e instanceof Error ? e.message : e);
  process.exit(0);
}

const basePath = pkg ? `/api/v1/web/${pkg}` : "";
const atRoot =
  process.env.LLM_STATIC_WEB_AT_ROOT === "1" ||
  /^true$/i.test(String(process.env.LLM_STATIC_WEB_AT_ROOT || ""));
const spaBasename = atRoot ? "" : pkg ? `/${pkg}` : "";

const content = `/* eslint-disable */
// AUTO-GENERATED by scripts/sync-web-actions-base.mjs from app.config.yaml — do not edit.
// Regenerate: npm run sync:web-actions-base (also runs on postinstall / pre app:dev / pre app:build).
export const LLM_WEB_ACTIONS_BASE_PATH = ${JSON.stringify(basePath)};
`;

const basenameContent = `/* eslint-disable */
// AUTO-GENERATED by scripts/sync-web-actions-base.mjs from app.config.yaml — do not edit.
// Same OpenWhisk package segment as LLM_WEB_ACTIONS_BASE_PATH when the SPA is namespaced on the CDN.
// When empty (LLM_STATIC_WEB_AT_ROOT=1 at sync time), the SPA is intentionally at the static origin root
// (single app per host). Do not use that mode if several apps share one static host and rely on /package/ isolation.
// BrowserRouter: pass as React Router basename (see app.jsx). HashRouter: do not use as basename —
// routes live in the hash; the document path may still use a prefix if Parcel publicUrl is not "/".
export const LLM_SPA_BASENAME = ${JSON.stringify(spaBasename)};
`;

fs.mkdirSync(path.dirname(outFile), { recursive: true });
fs.writeFileSync(outFile, content, "utf8");
fs.writeFileSync(basenameFile, basenameContent, "utf8");

try {
  const pj = JSON.parse(fs.readFileSync(webPackageJson, "utf8"));
  const publicUrl = atRoot ? "/" : pkg ? `/${pkg}/` : "/";
  const distDir = atRoot || !pkg ? "./dist" : `./dist/${pkg}`;
  pj.targets = pj.targets && typeof pj.targets === "object" ? pj.targets : {};
  pj.targets.default = { ...(pj.targets.default || {}), publicUrl, distDir };
  // `@adobe/aio-lib-web` builds with Parcel target name `webassets` (see bundle.js); it does not use
  // `targets.default`, so publicUrl must be duplicated here or aio builds emit `/web-src.*` at `/`.
  if (atRoot || !pkg) {
    delete pj.targets.webassets;
  } else {
    pj.targets.webassets = { ...(pj.targets.webassets || {}), publicUrl };
  }
  fs.writeFileSync(webPackageJson, JSON.stringify(pj, null, 2) + "\n");
  console.log(
    "[sync-web-actions-base] set web-src package.json targets.default.publicUrl →",
    publicUrl,
    "distDir →",
    distDir,
    atRoot || !pkg ? "(no webassets target)" : "+ targets.webassets.publicUrl"
  );
} catch (e) {
  console.warn("[sync-web-actions-base] could not update web-src/package.json targets:", e.message || e);
}

if (atRoot && pkg) {
  console.log(
    "[sync-web-actions-base] LLM_STATIC_WEB_AT_ROOT: SPA basename empty, publicUrl /, distDir ./dist (API path still",
    basePath + "). Omit this flag when multiple SPAs share one static origin and need /" + pkg + "/ isolation."
  );
}
if (pkg) {
  console.log("[sync-web-actions-base] wrote", path.relative(root, outFile), "→", basePath);
  console.log("[sync-web-actions-base] wrote", path.relative(root, basenameFile), "→", JSON.stringify(spaBasename));
} else {
  console.warn("[sync-web-actions-base] no package under runtimeManifest.packages; wrote empty LLM_WEB_ACTIONS_BASE_PATH");
}
